import UIKit

for x in 0...100 {
    if x % 5 == 0 {
        print("\(x) bingo!!!")
    }
    if x % 2 == 0 {
        print("\(x) numero par!!!")
    }
    if x % 2 == 1 {
        print("\(x) numero impar!!!")
    }
    if x >= 30 && x <= 40 {
        print("\(x) viva swift!!!")
    }
}

